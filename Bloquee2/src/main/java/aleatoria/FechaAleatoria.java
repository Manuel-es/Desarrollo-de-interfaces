    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int targetYear = 2025;

        int daysInYear = Year.of(targetYear).length();

        int randomDayOfYear = ThreadLocalRandom.current().nextInt(1, daysInYear + 1);

        LocalDate randomDate = LocalDate.ofYearDay(targetYear, randomDayOfYear);
       
        DayOfWeek dayOfWeek = randomDate.getDayOfWeek();
       
        Locale spanishLocale = new Locale("es", "ES");
       
        String dayName = dayOfWeek.getDisplayName(TextStyle.FULL, spanishLocale);

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        String formattedDate = randomDate.format(formatter);

        String message = "Fecha: " + formattedDate + "\n"
                       + "DÃ­a de la semana: " + dayName;
       
        JOptionPane.showMessageDialog(this,
                                      message,
                                      "Fecha Aleatoria",
                                      JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed

